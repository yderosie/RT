static float		fade(float t)
{
	return (t * t * t * (t * (t * 6 - 15) + 10));
}

static float		lerp(float t, float a, float b)
{
	return (a + t * (b - a));
}

static float		grad(int hash, float x, float y, float z)
{
	int		h;
	float	u;
	float	v;

	h = hash & 15;
	u = h < 8 || h == 12 || h == 13 ? x : y;
	v = h < 4 || h == 12 || h == 13 ? y : z;
	return (((h & 1) == 0 ? u : -u) + ((h & 2) == 0 ? v : -v));
}

float				perlin_noise(float x, float y, float z)
{
	t_perlin p;

	p.xx = (int)floor(x) & 255;
	p.yy = (int)floor(y) & 255;
	p.zz = (int)floor(z) & 255;
	x -= floor(x);
	y -= floor(y);
	z -= floor(z);
	p.u = fade(x);
	p.v = fade(y);
	p.w = fade(z);
	p.a = g_p[p.xx] + p.yy;
	p.aa = g_p[p.a] + p.zz;
	p.ab = g_p[p.a + 1] + p.zz;
	p.b = g_p[p.xx + 1] + p.yy;
	p.ba = g_p[p.b] + p.zz;
	p.bb = g_p[p.b + 1] + p.zz;
	return (lerp(p.w, lerp(p.v, lerp(p.u, grad(g_p[p.aa], x, y, z),
									grad(g_p[p.ba], x - 1, y, z)),
								lerp(p.u, grad(g_p[p.ab], x, y - 1, z),
									grad(g_p[p.bb], x - 1, y - 1, z))),
						lerp(p.v, lerp(p.u, grad(g_p[p.aa + 1], x, y, z - 1),
									grad(g_p[p.ba + 1], x - 1, y, z - 1)),
								lerp(p.u, grad(g_p[p.ab + 1], x, y - 1, z - 1),
								grad(g_p[p.bb + 1], x - 1, y - 1, z - 1)))));
}

t_color3			fk_perlin(t_intersect inter)
{
	float			perlin;
	float			v[3];

	v[0] = inter.obj->mat.v0;
	v[1] = inter.obj->mat.v1;
	v[2] = inter.obj->mat.v2;
	perlin = fabs(perlin_noise(inter.pos.x, inter.pos.y, inter.pos.z));
	if (perlin < v[0])
		return (inter.obj->mat.color1);
	else if (perlin > v[0] && perlin < v[1])
		return (vector_sum(vector_mul(inter.obj->mat.color1,
					(perlin - v[0]) / (v[1] - v[0])),
					vector_mul(inter.obj->mat.color2,
					(v[1] - perlin) / (v[1] - v[0]))));
	else if (perlin > v[1] && perlin < v[2])
	{
		return (vector_sum(vector_mul(inter.obj->mat.color2,
				(perlin - v[1]) / (v[2] - v[1])),
					vector_mul(inter.obj->mat.color3,
						(v[2] - perlin) / (v[2] - v[1]))));
	}
	return (inter.obj->mat.color3);
}
